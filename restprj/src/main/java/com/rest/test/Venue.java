package com.rest.test;

public class Venue {

	private int id;
	private String name;
	private String localityMap;
	private String spaceMap;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLocalityMap() {
		return localityMap;
	}
	public void setLocalityMap(String localityMap) {
		this.localityMap = localityMap;
	}
	public String getSpaceMap() {
		return spaceMap;
	}
	public void setSpaceMap(String spaceMap) {
		this.spaceMap = spaceMap;
	}
	
	
	
}
